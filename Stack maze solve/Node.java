public class Node {
    int x;
    int y;

    //This method is the node constructor
    public Node(int d, int f)
    {
        x = d;
        y = f;
    }

    //This method gets the x value of the node
    public int getX()
    {
        return x;
    }

    //This method gets the y value of the node
    public int getY()
    {
        return y;
    }
}
